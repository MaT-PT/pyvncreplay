    @classmethod
    def get_name(cls, value: int, raw_chars: bool = False) -> str:
        try:
            return cls(value).get_char(raw_chars=raw_chars)
        except ValueError:
            return f"[Unknown: 0x{value:X}]"

    def get_char(self, raw_chars: bool = True) -> str:
        if raw_chars:
            match self:
                case XKey.XK_Tab:
                    return "\t"
                case XKey.XK_Linefeed | XKey.XK_Return:
                    return "\n"
                case XKey.XK_Shift_L | XKey.XK_Shift_R:
                    return ""

        # ASCII/ISO-8859-1 printable characters
        if 0x20 <= self <= 0x7E or 0xA0 <= self <= 0xFF:
            if not raw_chars:
                if self.value == 0x20:
                    return "[Space]"
                if self.value == 0xA0:
                    return "[NBSP]"
            return chr(self.value)

        name = self.name.removeprefix("XK_")
        return f"[{name}]"

    def __str__(self) -> str:
        return self.get_char(raw_chars=False)
